n1 = int(input('Digite um número: '))
n2 = int(input('Digite outro número: '))
s = n1 + n2
sub = n1 - n2
m = n1 * n2
ex = n1 ** n2
div = n1 / n2
divi = n1 // n2
print('A soma é {}, a subtração é {}, a multiplicação é {}, a potencia é {}'.format(s, sub, m, ex))
print('A divisão é {} a divisão não inteira é {}'.format(div, divi))